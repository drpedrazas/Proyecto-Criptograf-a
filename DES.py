def bintohex(b):
    mp = {"0000" : '0',
          "0001" : '1',
          "0010" : '2',
          "0011" : '3',
          "0100" : '4',
          "0101" : '5',
          "0110" : '6',
          "0111" : '7',
          "1000" : '8',
          "1001" : '9',
          "1010" : 'A',
          "1011" : 'B',
          "1100" : 'C',
          "1101" : 'D',
          "1110" : 'E',
          "1111" : 'F' }
    return ''.join([mp[b[i:i+4]] for i in range(0,len(b),4)])

def dectobin(d):
    res = bin(d).replace("0b", "")
    print(res)
    res = "0"*(4-len(res)%4) + res if len(res)%4 != 0 else res
    return res

